    public void addVendor() throws SQLException{
        int phone = Integer.parseInt(jtf_phone.getText());
        int phone2 = Integer.parseInt(jtf_phone2.getText());
        int mobile = Integer.parseInt(jtf_mobile.getText());
        int fax = Integer.parseInt(jtf_fax.getText());
        try {
            _db.dbCon();
            String sql = "INSERT INTO public.pu_pur_ven(pur_ven_name, pur_ven_title, pur_ven_adress, pur_ven_person, pur_ven_phone, pur_ven_phone_2, pur_ven_mobile, pur_ven_fax, pur_ven_email, pur_ven_web) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            psmt = _db.connection.prepareStatement(sql);
            psmt.setString(1, jtf_name.getText());
            psmt.setString(2, jtf_title.getText());
            psmt.setString(3, jtf_adress.getText());
            psmt.setString(4, jtf_person.getText());
            psmt.setInt(5, phone);
            psmt.setInt(6, phone2);
            psmt.setInt(7, mobile);
            psmt.setInt(8, fax);
            psmt.setString(9, jtf_email.getText());
            psmt.setString(10, jtf_webSite.getText());
            psmt.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Satıcı Kayıdı Yapılmıştır");
        } catch (SQLException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Satıcı Kayıdı Yapılamamıştır !");
        }
    }

